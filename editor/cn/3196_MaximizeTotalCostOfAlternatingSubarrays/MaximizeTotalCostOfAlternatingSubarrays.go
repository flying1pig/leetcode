func maximumTotalCost(a []int) int64 {
	n := len(a)
	memo := make([]int, n)
	for i := range memo {
		memo[i] = math.MinInt
	}
	var dfs func(int) int
	dfs = func(i int) int {
		if i < 0 {
			return 0
		}
		if i == 0 {
			return a[0]
		}
		p := &memo[i]
		if *p != math.MinInt {
			return *p
		}
		*p = max(dfs(i-1)+a[i], dfs(i-2)+a[i-1]-a[i])
		return *p
	}
	return int64(dfs(n - 1))
}

