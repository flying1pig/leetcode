func minDeletionSize(strs []string) (res int) {
	n := len(strs)
	check := func() int {
		for i := 1; i < n; i++ {
			if strs[i-1] > strs[i] {
				return i
			}
		}
		return n
	}

	for i := check(); i < n; i = check() {
		res++
		for j := 0; j < len(strs[i]); j++ {
			if strs[i-1][j] > strs[i][j] {
				for i := range strs {
					strs[i] = strs[i][:j] + strs[i][j+1:]
				}
				break
			}
		}
	}
	return
}

